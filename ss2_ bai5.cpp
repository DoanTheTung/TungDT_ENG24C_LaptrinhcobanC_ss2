#include<stdio.h> 

int main() {
	//Khai bao bien cdai và crong cua hcn 
	int a = 3;
	int b = 2; 
	//Khai bao bien nho ket qua
	int chuvi , dientich; 
	//tinh chuvi cua hcn 
	chuvi = (a + b) * 2;
	//tinh dientich cua hcn 
	dientich = a * b; 
	//In ra ket qua
	printf("chuvi  hcn la %d\n", chuvi);
	printf("dientich hcn là %d\n", dientich);
	
	return 0;
}
